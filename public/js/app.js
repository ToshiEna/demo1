            showError('Èü≥Â£∞Ê©üËÉΩ„ÅØÂà©Áî®„Åß„Åç„Åæ„Åõ„Çì„ÄÇAzure Speech Services„ÅÆË®≠ÂÆö„ÇíÁ¢∫Ë™ç„Åó„Å¶„Åè„Å†„Åï„ÅÑ„ÄÇ');
