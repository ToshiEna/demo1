/**
 * Play a single message in sequence
 * @param {string} text - Text to play
 * @param {string} agentType - 'shareholder' or 'company'
 * @returns {Promise} Promise that resolves when audio finishes
 */
async function playMessageSequentially(text, agentType) {
    return new Promise(async (resolve, reject) => {
        try {
            // Request audio from server
            const response = await fetch('/api/voice/text-to-speech', {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json'
                },
                body: JSON.stringify({
                    text: text,
                    agentType: agentType
                })
            });
            
            if (!response.ok) {
                const error = await response.json();
                throw new Error(error.error || 'Èü≥Â£∞ÁîüÊàê„Å´Â§±Êïó„Åó„Åæ„Åó„Åü„ÄÇ');
            }
            
            // Create audio blob and play
            const audioBlob = await response.blob();
            const audioUrl = URL.createObjectURL(audioBlob);
            
            const audio = new Audio(audioUrl);
            
            audio.onended = () => {
                URL.revokeObjectURL(audioUrl);
                resolve();
            };
            
            audio.onerror = () => {
                URL.revokeObjectURL(audioUrl);
                reject(new Error('Èü≥Â£∞„ÅÆÂÜçÁîü„Å´Â§±Êïó„Åó„Åæ„Åó„Åü„ÄÇ'));
            };
            
            await audio.play();
            
        } catch (error) {
            reject(error);
        }
    });
}

